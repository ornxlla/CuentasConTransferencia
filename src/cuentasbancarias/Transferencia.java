package cuentasbancarias;

public class Transferencia {
	public Integer numeroDeTransferencia = 0;
	public Double cantidadQueSeTransfirio = 0.0;
	
	public Integer getNumeroDeTransferencia() {
		return numeroDeTransferencia;
	}
	public void setNumeroDeTransferencia(Integer numeroDeTransferencia) {
		this.numeroDeTransferencia = numeroDeTransferencia;
	}
	public Double getCantidadQueSeTransfirio() {
		return cantidadQueSeTransfirio;
	}
	public void setCantidadQueSeTransfirio(Double cantidadQueSeTransfirio) {
		this.cantidadQueSeTransfirio = cantidadQueSeTransfirio;
	}
	
}
